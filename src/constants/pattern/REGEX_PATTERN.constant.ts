export enum Pattern {
  EMAIL = '^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$',
  PASSWORD = '^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d@$!%*?&]{8,}$',
  PHONE_NUMBER = '^09\\d{9}$',
  USERNAME = '^[a-zA-Z0-9_-]{3,16}$',
  URL = '^(https?:\\/\\/)?([\\w\\d-]+\\.)+[\\w-]{2,4}(\\/.*)?$',
  POSTAL_CODE = '^\\d{10}$',
  UUID = '^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$',
  HEX_COLOR = '^#?([a-f0-9]{6}|[a-f0-9]{3})$',
  IPV4 = '^(25[0-5]|2[0-4]\\d|[0-1]?\\d{1,2})(\\.(25[0-5]|2[0-4]\\d|[0-1]?\\d{1,2})){3}$',
  SHEBA_NUMBER = '/^IR[0-9]{24}$/',
}
